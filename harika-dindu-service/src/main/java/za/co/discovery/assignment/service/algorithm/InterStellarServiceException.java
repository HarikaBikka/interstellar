package za.co.discovery.assignment.service.algorithm;

public class InterStellarServiceException extends RuntimeException {

	public InterStellarServiceException() {
		super();
		 
	}

	public InterStellarServiceException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		 
	}

	public InterStellarServiceException(String message, Throwable cause) {
		super(message, cause);
		 
	}

	public InterStellarServiceException(String message) {
		super(message);
		 
	}

	public InterStellarServiceException(Throwable cause) {
		super(cause);
		 
	}

	/**
	 * 
	 */
	private static final long serialVersionUID = -7824971776677366068L;

}
